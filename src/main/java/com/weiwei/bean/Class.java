package com.weiwei.bean;

public class Class {
    
    //会员等级
    private String classId;
    private String className;
    private String classDownload;
    private String classUpload;

    public String getClassId() {
        return classId;
    }

    public void setClassId(String classId) {
        this.classId = classId;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getClassDownload() {
        return classDownload;
    }

    public void setClassDownload(String classDownload) {
        this.classDownload = classDownload;
    }

    public String getClassUpload() {
        return classUpload;
    }

    public void setClassUpload(String classUpload) {
        this.classUpload = classUpload;
    }

    public Class() {
    }



}
